package bascic1;

public class Solutioncons {
	int a, b, c, d, e;
	public Solutioncons(int y1, int y2, int y3, int y4, int y5) {
		a = y1;
		b = y2;
		c = y3;
		d = y4;
		e = y5;
		
	}
	
	public static void main(String[] args) {
		Solutioncons obj = new Solutioncons(12, 14, 16, 18, 20);
		System.out.println(obj.a);
		System.out.println(obj.b);
		System.out.println(obj.c);
		System.out.println(obj.d);
		System.out.println(obj.e);

	}
}
